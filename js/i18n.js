// ─── Language Engine ──────────────────────────────────────────────────────────

const DEFAULT_LANG = 'vi';

function getLang() {
  return localStorage.getItem('vc_lang') || DEFAULT_LANG;
}

function setLang(code) {
  localStorage.setItem('vc_lang', code);
  applyLang(code);
}

function applyLang(code) {
  const t = TRANSLATIONS[code] || TRANSLATIONS[DEFAULT_LANG];

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = resolvePath(t, key);
    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    }
  });

  // data-i18n-placeholder (explicit placeholder override)
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = resolvePath(t, key);
    if (val !== undefined) el.placeholder = val;
  });

  // Update lang-block visibility
  document.querySelectorAll('.lang-block').forEach(el => {
    el.style.display = el.getAttribute('data-lang') === code ? '' : 'none';
  });

  // Update html lang attribute
  document.documentElement.lang = code === 'vi' ? 'vi' : 'en';

  // Update new dropdown lang switcher active state
  document.querySelectorAll('.lang-switcher__option').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang-code') === code);
  });
  // Update switcher button label
  document.querySelectorAll('.lang-switcher__current').forEach(el => {
    const flag = code === 'vi' ? '🇻🇳' : '🇺🇸';
    el.textContent = flag + ' ' + code.toUpperCase();
  });

  // Legacy: old pill lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('lang-btn--active', btn.getAttribute('data-lang-code') === code);
  });
}

function resolvePath(obj, path) {
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  applyLang(getLang());

  // New dropdown lang switcher
  document.querySelectorAll('.lang-switcher').forEach(switcher => {
    const toggleBtn = switcher.querySelector('.lang-switcher__btn');
    if (toggleBtn) {
      toggleBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        switcher.classList.toggle('open');
      });
    }
    switcher.querySelectorAll('.lang-switcher__option').forEach(opt => {
      opt.addEventListener('click', () => {
        setLang(opt.getAttribute('data-lang-code'));
        switcher.classList.remove('open');
      });
    });
  });

  // Close dropdown on outside click
  document.addEventListener('click', () => {
    document.querySelectorAll('.lang-switcher.open').forEach(s => s.classList.remove('open'));
  });

  // Legacy pill lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.getAttribute('data-lang-code')));
  });
});
